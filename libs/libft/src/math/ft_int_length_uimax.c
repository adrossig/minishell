#include "../../includes/libft.h"

int	ft_int_length_uimax(uintmax_t n)
{
	int	i;

	if (n < 0)
		n *= -1;
	i = 0;
	while (n >= 10)
	{
		n /= 10;
		i++;
	}
	return (1 + i);
}
